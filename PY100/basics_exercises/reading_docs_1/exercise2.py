# Determine whether Python has a method to lowercase a string, for
# example converting 'Aloha, World!' into 'aloha, world!.

# It does! It's a string method called lower() which returns a copy of
# the string with all of the cased characters convereted to lowercase.
# Can be found here: https://docs.python.org/3/library/stdtypes.html#str.lower